function getSum() {
    var x = document.getElementById("x");
    var y = document.getElementById("y");
    var sum = parseInt(x.value) + parseInt(y.value);
    alert(sum);
}

function getSub() {
    var x = document.getElementById("x");
    var y = document.getElementById("y");
    var sub = parseInt(x.value) - parseInt(y.value);
    alert(sub);
}

function getMult() {
    var x = document.getElementById("x");
    var y = document.getElementById("y");
    var mult = parseInt(x.value) * parseInt(y.value);
    alert(mult);
}

function getDiv() {
    var x = document.getElementById("x");
    var y = document.getElementById("y");
    var div = parseInt(x.value) / parseInt(y.value);
    alert(div);
}

